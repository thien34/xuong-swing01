    public void loadModel(List<SanXuat> list) {
        model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        list.forEach(o -> {
            model.addRow(new Object[]{
                o.getMaNSX(),
                o.getTenNSX(),
                o.getDiaChi(),
                o.getTrangThai()
            });
        });
    }

    void setModel(SanXuat o) {
        jTextField1.setText(o.getMaNSX());
        jTextField2.setText(o.getTenNSX());
        jTextField3.setText(o.getDiaChi());
        jRadioButton1.setSelected(o.getTrangThai());
    }

    SanXuat getModel() {
        return SanXuat.builder()
                .maNSX(jTextField1.getText())
                .tenNSX(jTextField2.getText())
                .diaChi(jTextField3.getText())
                .trangThai(jRadioButton1.isSelected())
                .build();
    }

